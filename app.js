/**
 * Module dependencies.
 */

var express = require('express'),
    MongoStore = require('connect-mongo')(express),  // MongoDB session store for Connect
    flash = require('express-flash'), // an extension of connect-flash with the ability to define a flash message and render it without redirecting the request
    path = require('path'),
    mongoose = require('mongoose'),
    passport = require('passport');
    expressValidator = require('express-validator'), // validator.js is a library of string validators and sanitizers
    connectAssets = require('connect-assets'), // Transparent file compilation and dependency management for node's connect framework in the spirit of hte Rails 3.1 asset pipeline
    secrets = require('./config/secrets'),
    passportConf = require('./config/passport');

/**
 * Create Express server.
 */

var app = express();

/**
 * Mongoose configuration.
 */

mongoose.connect(secrets.db);
mongoose.connection.on('error', function() {
  console.error('MongoDB Connection Error. Please make sure MongoDB is running.');
});

/**
 * Express configuration.
 */

var hour = 3600000;
var day = (hour * 24);
var week = (day * 7);
var month = (day * 30);

app.set('port', process.env.PORT || 3000);
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'jade');
app.use(connectAssets({
  paths: ['public/css', 'public/js'],
  helperContext: app.locals
}));
app.use(express.compress());
app.use(express.favicon());
app.use(express.logger('dev'));
app.use(express.cookieParser());
app.use(express.json());
app.use(express.urlencoded());
app.use(expressValidator());
app.use(express.methodOverride());
app.use(express.session({
  secret: secrets.sessionSecret,
  store: new MongoStore({
    db: mongoose.connection.db,
    auto_reconnect: true
  })
}));
app.use(express.csrf());
app.use(passport.initialize());
app.use(passport.session());
app.use(function(req, res, next) {
  res.locals.user = req.user;
  res.locals.token = req.csrfToken();
  res.locals.secrets = secrets;
  next();
});
app.use(flash());
app.use(app.router);
app.use(express.static(path.join(__dirname, 'public'), { maxAge: week }));
app.use(function(req, res) {
  res.status(404);
  res.render('404');
});
app.use(express.errorHandler());

/**
 * Application routes.
 */
require('./routes')(app, passportConf, passport),


/**
 * Start Express server.
 */

app.listen(app.get('port'), function() {
  console.log("âœ” Express server listening on port %d in %s mode", app.get('port'), app.settings.env);
});
